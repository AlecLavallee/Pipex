#include "libft.h"

char			*ft_strtrim(char const *s1, char const *set)
{
	size_t	len;
	char	*str;

	if (!s1)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len && ft_strchr(set, s1[len - 1]))
		len--;
	if (!(str = (char*)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	str[len] = '\0';
	return (ft_memcpy(str, s1, len));
}
